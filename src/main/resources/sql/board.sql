CREATE TABLE NOTICE(
	NUM NUMBER(8) CONSTRAINT NOTICE_NUM_PK PRIMARY KEY,
	TITLE VARCHAR2(400) CONSTRAINT NOTICE_TITLE_NN NOT NULL,
	WRITER VARCHAR2(50) CONSTRAINT NOTICE_WRITER_NN NOT NULL,
	CONTENTS CLOB,
	REG_DATE DATE,
	HIT NUMBER(8)
);

CREATE TABLE FILES(
	NUM NUMBER(8) CONSTRAINT FILES_NUM_FK REFERENCES NOTICE(NUM) ON DELETE CASCADE,
	FNUM NUMBER(8) CONSTRAINT FILES_FNUM_PK PRIMARY KEY,
	FNAME VARCHAR2(400) CONSTRAINT FILES_FNAME_NN NOT NULL,
	ONAME VARCHAR2(400) CONSTRAINT FILES_ONAME_NN NOT NULL
);

CREATE SEQUENCE NOTICE_SEQ
NOCACHE
NOMAXVALUE
NOCYCLE;

CREATE SEQUENCE FILES_SEQ
NOCACHE
NOMAXVALUE
NOCYCLE;

CREATE TABLE MEMBERFILE(
	ID VARCHAR2(100) CONSTRAINT MEMBERFILE_ID_FK REFERENCES MEMBER(ID) ON DELETE CASCADE,
	NUM NUMBER(8) CONSTRAINT MEMBERFILE_NUM_PK PRIMARY KEY,
	FNAME VARCHAR2(400) CONSTRAINT MEMBERFILE_FNAME_NN NOT NULL,
	ONAME VARCHAR2(400) CONSTRAINT MEMBERFILES_ONAME_NN NOT NULL
);

CREATE SEQUENCE MEMBERFILE_SEQ
NOCACHE
NOMAXVALUE
NOCYCLE;
